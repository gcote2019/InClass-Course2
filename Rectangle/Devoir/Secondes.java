package Rectangle.Devoir;

public class Secondes {
    public static void main(String[] args) {
        int Heure = 3;
        int Minutes = 4;
        final int SecondsInAnHour = 3600;
        final int SecondsInAnMinute = 60;
        int secondes = Heure * SecondsInAnHour + Minutes * SecondsInAnMinute;

        System.out.println("Sa fait " + secondes + " Secondes!");
    }
}

