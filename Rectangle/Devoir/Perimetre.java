package Rectangle.Devoir;

public class Perimetre {
    public static void main(String[] args) {
        double x = 4.5;
        double y = 3;

        double Perimetre;
        double Aire;

        Aire = 2 * x + 2 * y;
        Perimetre = x * y;

        System.out.println("Le Perimetre est " + Perimetre);
        System.out.println("L'Aire est " + Aire);
    }
}
